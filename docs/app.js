const toTitle = (str) => str.replace(/([a-z])([A-Z])/g, "$1 $2");

function detectType(name, category) {
    const lower = `${name} ${category}`.toLowerCase();
    if (lower.includes("extension") || lower.includes("util") || lower.includes("modifier") || lower.includes("style")) {
        return "Utility";
    }
    if (lower.includes("controller")) {
        return "Controller";
    }
    if (lower.includes("cell")) {
        return "Cell";
    }
    return "View";
}

function buildComponents(files, framework) {
    return files.map((filePath) => {
        const parts = filePath.split("/");
        const fileName = parts[parts.length - 1];
        const name = fileName.replace(/\.swift$/, "");
        const frameworkRoot = framework === "UIKit" ? "CustomView" : "SwiftUIView";
        const frameworkIndex = parts.indexOf(frameworkRoot);
        const category = frameworkIndex >= 0
            ? parts.slice(frameworkIndex + 1, parts.length - 1).join(" / ")
            : frameworkRoot;
        const type = detectType(name, category);
        return {
            id: `${framework.toLowerCase()}-${name.toLowerCase()}`,
            name,
            displayName: toTitle(name),
            filePath,
            category: category || frameworkRoot,
            framework,
            type,
            summary: `${name} ${type.toLowerCase()} for ${framework}.`
        };
    });
}

