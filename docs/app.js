function renderFrameworkTabs() {
    frameworkTabsEl.innerHTML = "";
    frameworks.forEach((framework) => {
        const button = document.createElement("button");
        button.textContent = framework;
        button.className = framework === state.framework ? "active" : "";
        button.addEventListener("click", () => {
            state.framework = framework;
            state.category = "All";
            state.activeId = null;
            render();
        });
        frameworkTabsEl.appendChild(button);
    });
}

function renderCategories(categories) {
    categoryEl.innerHTML = "";
    categories.forEach((category) => {
        const button = document.createElement("button");
        button.textContent = category;
        button.className = category === state.category ? "active" : "";
        button.addEventListener("click", () => {
            state.category = category;
            render();
        });
        categoryEl.appendChild(button);
    });
}

function usageSnippet(component) {
    if (component.framework === "UIKit") {
        if (component.name.endsWith("ViewController")) {
            return `let viewController = ${component.name}()\nself.navigationController?.pushViewController(viewController, animated: true)`;
        }
        if (component.name.endsWith("TableViewCell")) {
            return `tableView.register(${component.name}.self, forCellReuseIdentifier: "${component.name}")\nlet cell = tableView.dequeueReusableCell(withIdentifier: "${component.name}", for: indexPath) as! ${component.name}`;
        }
        if (component.name.endsWith("CollectionViewCell")) {
            return `collectionView.register(${component.name}.self, forCellWithReuseIdentifier: "${component.name}")\nlet cell = collectionView.dequeueReusableCell(withReuseIdentifier: "${component.name}", for: indexPath) as! ${component.name}`;
        }
        return `let view = ${component.name}()\nview.translatesAutoresizingMaskIntoConstraints = false\nparentView.addSubview(view)`;
    }

    return `struct DemoView: View {\n    var body: some View {\n        ${component.name}()\n    }\n}`;
}

function exampleSnippet(component) {
    if (component.framework === "SwiftUI") {
        return `// Example usage\n${component.name}()\n    .padding()\n    .background(Color.black.opacity(0.12))`;
    }
    return `// Example configuration\n// Set properties as needed\n// Layout with Auto Layout or frame`;
}

function illustrationSvg(component) {
    const hash = component.name.split("").reduce((acc, ch) => acc + ch.charCodeAt(0), 0);
    const hue = hash % 360;
    const hue2 = (hue + 70) % 360;
    const initials = component.name.replace(/[^A-Z]/g, "").slice(0, 2) || component.name.slice(0, 2);
    return `
        <svg width="76" height="76" viewBox="0 0 76 76" fill="none" xmlns="http://www.w3.org/2000/svg">
            <defs>
                <linearGradient id="grad" x1="0" y1="0" x2="1" y2="1">
                    <stop offset="0" stop-color="hsl(${hue}, 72%, 55%)"/>
                    <stop offset="1" stop-color="hsl(${hue2}, 72%, 45%)"/>
                </linearGradient>
            </defs>
            <rect x="6" y="6" width="64" height="64" rx="16" fill="url(#grad)"/>
            <text x="38" y="44" font-size="18" font-family="IBM Plex Sans, SF Pro Text, sans-serif" text-anchor="middle" fill="#0b0e14">${initials}</text>
        </svg>
    `;
}

function renderList(filtered) {
    listEl.innerHTML = "";
    filtered.forEach((component, index) => {
        const card = document.createElement("div");
        card.className = `component-card ${component.id === state.activeId ? "active" : ""}`;
        card.style.animationDelay = `${Math.min(index * 0.02, 0.3)}s`;
        card.innerHTML = `
            <h4>${component.name}</h4>
            <p>${component.category} · ${component.type}</p>
        `;
        card.addEventListener("click", () => {
            state.activeId = component.id;
            renderDetail(component);
            renderList(filtered);
        });
        listEl.appendChild(card);
    });
}

function renderDetail(component) {
    if (!component) {
        detailEl.innerHTML = "<p>No matching component found.</p>";
        return;
    }
    detailEl.innerHTML = `
        <div class="detail-header">
            <div class="illustration">${illustrationSvg(component)}</div>
            <div class="detail-title">
                <h2>${component.name}</h2>
                <div class="meta">${component.framework} · ${component.category} · ${component.type}</div>
            </div>
        </div>
        <div class="detail-section">
            <h3>Overview</h3>
            <p>${component.summary}</p>
        </div>
        <div class="detail-section">
            <h3>Usage</h3>
            <pre><code>${usageSnippet(component)}</code></pre>
        </div>
        <div class="detail-section">
            <h3>Example</h3>
            <pre><code>${exampleSnippet(component)}</code></pre>
        </div>
        <div class="detail-section">
            <h3>Source</h3>
            <p><code>${component.filePath}</code></p>
        </div>
        <div class="footer-note">
            For full API details, open the Swift file to inspect public properties and methods.
        </div>
    `;
}

function render() {
    const filteredByFramework = components.filter((component) => component.framework === state.framework);
    const categories = ["All", ...Array.from(new Set(filteredByFramework.map((c) => c.category))).sort()];
    renderCategories(categories);

    const filtered = filteredByFramework.filter((component) => {
        const matchesCategory = state.category === "All" || component.category === state.category;
        const matchesQuery = component.name.toLowerCase().includes(state.query)
            || component.category.toLowerCase().includes(state.query);
        return matchesCategory && matchesQuery;
    });

    const countText = `${filtered.length} of ${filteredByFramework.length} ${state.framework} components`;
    if (countTopEl) {
        countTopEl.textContent = countText;
    }
    if (countHeroEl) {
        countHeroEl.textContent = countText;
    }

    if (!filtered.find((c) => c.id === state.activeId)) {
        state.activeId = filtered[0]?.id;
    }
    renderList(filtered);
    renderDetail(filtered.find((c) => c.id === state.activeId));
}

searchEl.addEventListener("input", (event) => {
    state.query = event.target.value.trim().toLowerCase();
    render();
});

const heroButtons = document.querySelectorAll("[data-framework]");
heroButtons.forEach((button) => {
    button.addEventListener("click", (event) => {
        const targetFramework = event.currentTarget.getAttribute("data-framework");
        if (!targetFramework) {
            return;
        }
        state.framework = targetFramework;
        state.category = "All";
        state.activeId = null;
        render();
        const section = document.querySelector("#components");
        if (section) {
            section.scrollIntoView({ behavior: "smooth" });
        }
    });
});

renderFrameworkTabs();
render();
